# gpt/make_prompt.py

def make_prompt(apartment_name: str) -> str:
    return f"""
당신은 대한민국 최고의 부동산 투자 전문가입니다.

다음 아파트 이름을 기반으로 가치 평가를 해주세요:
- 아파트 이름: {apartment_name}

아래 기준에 따라 간결하게 정리해 주세요:
1. 실거주 매력도 (교통, 학군, 생활 인프라)
2. 투자 안정성 (입지, 수요, 향후 전망)
3. 브랜드/단지 규모에 따른 프리미엄
4. 이 아파트의 종합 한줄 평가

항목별 점수(10점 만점)도 함께 작성해주세요.

형식 예시:
---
📍 아파트명: 무슨무슨아파트

1. 🏠 실거주 매력도: 8점 - xxx
2. 💰 투자 안정성: 7점 - xxx
3. 🏢 브랜드/단지 규모: 9점 - xxx
4. 🧠 한줄평: "xxx"

총점: 24점 / 30점
---
    """.strip()
